-- GALLERY 테이블 생성
CREATE TABLE IF NOT EXISTS gallery (
    uid INT AUTO_INCREMENT PRIMARY KEY COMMENT '게시물 ID (Primary Key)',
    name VARCHAR(100) NOT NULL COMMENT '작성자',
    title VARCHAR(200) NOT NULL COMMENT '제목',
    content TEXT NOT NULL COMMENT '게시물 내용',
    regdate DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '등록일',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    updated_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
    INDEX idx_regdate (regdate)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci COMMENT='관전후기 게시판 테이블'; 